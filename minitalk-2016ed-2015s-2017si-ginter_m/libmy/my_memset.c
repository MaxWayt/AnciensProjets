#include "my.h"

void	*my_memset(void *ptr, int value, int size)
{
  int	i;
  char	*itr;

  itr = (char*)ptr;
  i = 0;
  while (i < size)
  {
    itr[i] = (char)value;
    ++i;
  }
  return (ptr);
}
