void	*my_memcpy(void *dest, void *src, int n)
{
  int	i;
  char	*d;
  char	*s;

  d = (char*)dest;
  s = (char*)src;
  i = 0;
  while (i < n)
  {
    d[i] = s[i];
    i++;
  }
  return (dest);
}
